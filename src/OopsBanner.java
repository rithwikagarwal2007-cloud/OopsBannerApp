/**
 * UC3 - Full OOPS Banner using String.join(" ", ...)
 
 */
public class stepoops{
    public static void main(String[] args) {

        // Hint: Use String.join(" ", ...) to join the parts of each line with spaces.
        System.out.println(String.join("   ", "   ***  ", "   ***  ", "  *****  ", "  ***** "));
        System.out.println(String.join("   ", " **   **", " **   **", " **   **", " **     "));
        System.out.println(String.join("   ", "**     **", "**     **", "**   **", "**      "));
        System.out.println(String.join("   ", "**     **", "**     **", "*****   ", " *****  "));
        System.out.println(String.join("   ", "**     **", "**     **", "**      ", "      **"));
        System.out.println(String.join("   ", " **   ** ", " **   ** ", "**      ", " **   **"));
        System.out.println(String.join("   ", "  ***    ", "  ***    ", "**      ", "   ***   "));

    }
}
